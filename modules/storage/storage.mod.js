/**
 * @ngdoc overview
 * @name storage
 *
 * @description
 * # Storage
 * Storage is the module of Barney used to set and get values from 3 different storage:
 *
 * - Local Storage;
 * - Cookies;
 * - jsObject.
 *
 * # Components
 * - {@link storage.BarneyStorage BarneyStorage}
 *
 * # Usage
 * To use **Storage** module you have to include the **Storage** javascript file in your index.html:
 *
 * <pre>
 *
 *	  <!-- Storage module -->
 *   <script type="text/javascript" src="./bower_components/barney/dist/storage.min.js"> 
 *
 * </pre>
 * 
 * # Init Storage
 * Before using Storage module, it must be initialized. 
 * To initialize Storage you can use the Storage method {@link storage.BarneyStorage#methods_init init}.
 */